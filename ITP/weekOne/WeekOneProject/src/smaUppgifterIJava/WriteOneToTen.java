package smaUppgifterIJava;

public class WriteOneToTen {
    public static void main(String[] args) {
        int tal=1;
        while(tal <= 10) {
            System.out.println(tal);
            tal++;
        }
        double tal2=99;
        while (tal2<=100){
            System.out.println(tal2);
            tal2++;
        }

        for (int i=1;i<=10;i++){
            System.out.println(i);
        }
    }
}
