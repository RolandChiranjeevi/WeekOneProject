public class CircleRadius {
    public static void main(String[] args) {
        double diameterOfCircle = 55.5;
        double radiusOfCircle = diameterOfCircle/2;
        double pi = 3.14;
        double omkretsOfCircle = diameterOfCircle*pi;
        double areaOfCircle = radiusOfCircle*radiusOfCircle*pi;
        System.out.println(radiusOfCircle);
        System.out.println(omkretsOfCircle);
        System.out.println(areaOfCircle);

    }
}
